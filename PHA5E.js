

// Get the box and cursor elements
const box2 = document.getElementById('box2');
const cursor2 = document.getElementById('cursor2');

// Add event listeners for mouse move
box2.addEventListener('mousemove', (e) => {
    const x = e.clientX - box2.offsetLeft;
    const y = e.clientY - box2.offsetTop;

    // Update the cursor position
    cursor2.style.top = `${y}px`;
    cursor2.style.left = `${x}px`;
    cursor2.style.transform = 'translate(0, 0)';
});

// Add event listeners for mouse out
box2.addEventListener('mouseout', () => {
    // Reset the cursor position to its original position
    cursor2.style.top = '50%';
    cursor2.style.left = '50%';
    cursor2.style.transform = 'translate(-50%, -50%)';
});

// Add event listeners for mouse leave
box2.addEventListener('mouseleave', () => {
    // Reset the cursor position to its original position
    cursor2.style.top = '50%';
    cursor2.style.left = '50%';
    cursor2.style.transform = 'translate(-50%, -50%)';
});





